create or replace function public.complete_supplier_booking(p_booking_id uuid)
returns public.supplier_bookings
language plpgsql
security definer
set search_path = public
as $$
declare
    booking public.supplier_bookings;
begin
    update public.supplier_bookings
    set status = 'completed',
        payment_status = 'paid',
        completed_at = coalesce(completed_at, now()),
        audit = audit || jsonb_build_object('event', 'booking_completed', 'at', now(), 'actor', auth.uid())
    where id = p_booking_id
      and (public.is_admin() or supplier_id in (select id from public.supplier_profiles where user_id = auth.uid()))
    returning * into booking;

    if booking.id is null then
        raise exception 'Booking not found or not allowed';
    end if;

    return booking;
end;
$$;

create or replace function public.create_supplier_monthly_payout(
    p_supplier_id uuid,
    p_period_start date,
    p_period_end date
)
returns public.supplier_payouts
language plpgsql
security definer
set search_path = public
as $$
declare
    payout public.supplier_payouts;
begin
    if not public.is_admin() then
        raise exception 'Only admins can create payout batches';
    end if;

    insert into public.supplier_payouts (
        supplier_id,
        period_start,
        period_end,
        gross_amount,
        commission_amount,
        net_amount,
        currency,
        payout_method
    )
    select
        p_supplier_id,
        p_period_start,
        p_period_end,
        coalesce(sum(total_amount), 0),
        coalesce(sum(commission_amount), 0),
        coalesce(sum(supplier_payout_amount), 0),
        coalesce(max(currency), 'USD'),
        coalesce((select payout_method from public.supplier_profiles where id = p_supplier_id), 'bank')
    from public.supplier_bookings
    where supplier_id = p_supplier_id
      and status = 'completed'
      and payment_status = 'paid'
      and payout_id is null
      and completed_at::date between p_period_start and p_period_end
    returning * into payout;

    update public.supplier_bookings
    set payout_id = payout.id,
        audit = audit || jsonb_build_object('event', 'attached_to_payout', 'payout_id', payout.id, 'at', now())
    where supplier_id = p_supplier_id
      and status = 'completed'
      and payment_status = 'paid'
      and payout_id is null
      and completed_at::date between p_period_start and p_period_end;

    return payout;
end;
$$;

create or replace function public.mark_supplier_payout_paid(
    p_payout_id uuid,
    p_reference text,
    p_receipt_url text default null
)
returns public.supplier_payouts
language plpgsql
security definer
set search_path = public
as $$
declare
    payout public.supplier_payouts;
begin
    if not public.is_admin() then
        raise exception 'Only admins can mark payouts paid';
    end if;

    update public.supplier_payouts
    set status = 'paid',
        reference = p_reference,
        receipt_url = p_receipt_url,
        paid_at = now()
    where id = p_payout_id
    returning * into payout;

    return payout;
end;
$$;

