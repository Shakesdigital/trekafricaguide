create or replace function public.effective_supplier_commission(p_supplier_id uuid, p_product_id uuid default null)
returns numeric
language sql
stable
as $$
    select coalesce(
        (select commission_rate from public.supplier_products where id = p_product_id),
        (select commission_rate from public.supplier_profiles where id = p_supplier_id),
        (select rate from public.supplier_commission_settings where setting_key = 'default'),
        18.00
    );
$$;

create or replace function public.calculate_supplier_booking_commission()
returns trigger
language plpgsql
as $$
declare
    rate numeric(5,2);
    commission numeric(12,2);
begin
    rate := public.effective_supplier_commission(new.supplier_id, new.product_id);
    commission := round(new.total_amount * (rate / 100), 2);

    new.commission_rate := rate;
    new.commission_amount := commission;
    new.supplier_payout_amount := round(new.total_amount - commission, 2);
    new.audit := coalesce(new.audit, '[]'::jsonb) || jsonb_build_object(
        'event', 'commission_calculated',
        'formula', 'supplier_payout_amount = total_amount - (total_amount * commission_rate / 100)',
        'total_amount', new.total_amount,
        'commission_rate', rate,
        'commission_amount', commission,
        'supplier_payout_amount', round(new.total_amount - commission, 2),
        'at', now()
    );

    return new;
end;
$$;

drop trigger if exists calculate_supplier_booking_commission_before_write on public.supplier_bookings;
create trigger calculate_supplier_booking_commission_before_write
before insert or update of total_amount, supplier_id, product_id
on public.supplier_bookings
for each row execute function public.calculate_supplier_booking_commission();

create or replace function public.queue_supplier_booking_notifications()
returns trigger
language plpgsql
as $$
declare
    supplier public.supplier_profiles;
    product public.supplier_products;
begin
    select * into supplier from public.supplier_profiles where id = new.supplier_id;
    select * into product from public.supplier_products where id = new.product_id;

    insert into public.supplier_notifications (supplier_id, type, title, body)
    values (
        new.supplier_id,
        'new_booking',
        'New booking received',
        concat('New ', product.title, ' booking for ', new.customer_name, '. Gross ', new.currency, ' ', new.total_amount, ', commission ', new.currency, ' ', new.commission_amount, ', net payout ', new.currency, ' ', new.supplier_payout_amount, '.')
    );

    insert into public.supplier_notification_outbox (supplier_id, booking_id, channel, recipient, subject, body)
    values (
        new.supplier_id,
        new.id,
        'email',
        supplier.contact_email,
        'New Trek Africa Guide booking',
        concat('You have a new booking for ', product.title, '. Net supplier payout after commission: ', new.currency, ' ', new.supplier_payout_amount, '.')
    );

    if supplier.phone is not null then
        insert into public.supplier_notification_outbox (supplier_id, booking_id, channel, recipient, body)
        values (
            new.supplier_id,
            new.id,
            'sms',
            supplier.phone,
            concat('Trek Africa Guide booking: ', product.title, '. Net payout ', new.currency, ' ', new.supplier_payout_amount, '.')
        );
    end if;

    return new;
end;
$$;

