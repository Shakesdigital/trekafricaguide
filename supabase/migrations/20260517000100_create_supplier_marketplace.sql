create or replace function public.is_admin()
returns boolean
language sql
stable
as $$
    select coalesce(auth.jwt() -> 'app_metadata' ->> 'role', '') in ('admin', 'super_admin')
        or coalesce(auth.jwt() -> 'user_metadata' ->> 'role', '') in ('admin', 'super_admin');
$$;

